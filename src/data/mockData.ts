// ============================================
// INTERFACES
// ============================================

export interface User {
  id: number;
  name: string;
  email: string;
  role: 'admin' | 'operator';
  avatar: string;
  permissions: string[];
  lastLogin: string;
  status: 'active' | 'inactive';
}

export interface Client {
  id: number;
  name: string;
  code: string;
  email: string;
  phone: string;
  storeCount: number;
  ordersThisMonth: number;
  revenue: number;
  status: 'active' | 'inactive';
  createdAt: string;
}

export interface ShopifyStore {
  id: number;
  clientId: number;
  clientName: string;
  name: string;
  domain: string;
  apiToken: string;
  courierName: string;
  contractCode: string;
  lastSync: string;
  syncStatus: 'synced' | 'syncing' | 'error';
  ordersTotal: number;
  status: 'connected' | 'disconnected';
}

export interface Courier {
  id: number;
  name: string;
  code: string;
  apiEndpoint: string;
  services: string[];
  contractCode: string;
  clientCode: string;
  status: 'active' | 'inactive';
}

export interface ShippingRule {
  id: number;
  name: string;
  storeId: number;
  storeName: string;
  conditions: { field: string; operator: string; value: string }[];
  courierName: string;
  service: string;
  priority: number;
  active: boolean;
}

export interface Order {
  id: number;
  orderNumber: string;
  shopifyId: string;
  storeName: string;
  storeId: number;
  clientName: string;
  customerName: string;
  customerEmail: string;
  customerPhone: string;
  address: string;
  city: string;
  zip: string;
  country: string;
  items: OrderItem[];
  totalAmount: number;
  paymentMethod: string;
  isCOD: boolean;
  codStatus: 'pending' | 'confirmed' | 'cancelled' | 'none';
  codDecisionBy?: string;
  codDecisionAt?: string;
  orderStatus: 'new' | 'processing' | 'shipped' | 'delivered' | 'cancelled' | 'returned';
  labelGenerated: boolean;
  labelUrl?: string;
  trackingNumber?: string;
  courierName?: string;
  shippingService?: string;
  createdAt: string;
  updatedAt: string;
}

export interface OrderItem {
  id: number;
  productName: string;
  sku: string;
  quantity: number;
  price: number;
  image: string;
  variant?: string;
}

export interface Product {
  id: number;
  name: string;
  sku: string;
  barcode: string;
  storeId: number;
  storeName: string;
  clientName: string;
  image: string;
  variants: string[];
  quantityAvailable: number;
  quantityReserved: number;
  quantityIncoming: number;
  minimumStock: number;
  price: number;
  status: 'in_stock' | 'low_stock' | 'out_of_stock';
}

export interface Shipment {
  id: number;
  orderId: number;
  orderNumber: string;
  trackingNumber: string;
  courierName: string;
  service: string;
  customerName: string;
  destination: string;
  status: 'created' | 'picked_up' | 'in_transit' | 'out_for_delivery' | 'delivered' | 'held_at_depot' | 'exception';
  events: TrackingEvent[];
  estimatedDelivery: string;
  createdAt: string;
}

export interface TrackingEvent {
  date: string;
  time: string;
  status: string;
  location: string;
  description: string;
}

export interface InventoryMovement {
  id: number;
  productName: string;
  sku: string;
  type: 'inbound' | 'outbound' | 'adjustment' | 'return';
  quantity: number;
  reason: string;
  performedBy: string;
  createdAt: string;
}

export interface ActivityLog {
  id: number;
  user: string;
  action: string;
  target: string;
  details: string;
  createdAt: string;
}

export interface Label {
  id: number;
  orderNumber: string;
  trackingNumber: string;
  courierName: string;
  customerName: string;
  destination: string;
  status: 'generated' | 'printed' | 'error';
  createdAt: string;
  printedAt?: string;
}

export interface DashboardStats {
  ordersToday: number;
  ordersPending: number;
  codToConfirm: number;
  labelsToday: number;
  shipmentsInTransit: number;
  shipmentsHeld: number;
  lowStockProducts: number;
  totalRevenue: number;
  revenueGrowth: number;
}

